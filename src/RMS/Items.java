    private void fooditem_tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fooditem_tblMouseClicked

        int row[] = this.fooditem_tbl.getSelectedRows();

        for (int i = 0; i < row.length; i++) {

            int modelRow = fooditem_tbl.convertRowIndexToModel(row[i]);
            String id = model.getValueAt(modelRow, 0).toString();
            String name = model.getValueAt(modelRow, 1).toString();
            String price = model.getValueAt(modelRow, 2).toString();
            String category = model.getValueAt(modelRow, 3).toString();
            id_txt.setText(id);
            itemname_txt.setText(name);
            price_txt.setText(price);
            category_cbox.setSelectedItem(category);

        }
    }//GEN-LAST:event_fooditem_tblMouseClicked

