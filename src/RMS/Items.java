    private void add_btnKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_add_btnKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!(Pattern.matches("^[a-zA-Z ]+$", itemname_txt.getText()))) {
                JOptionPane.showMessageDialog(this, "Please enter a valid item name", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (!(Pattern.matches("^[0-9]+$", id_txt.getText()))) {
                JOptionPane.showMessageDialog(this, "Please enter a valid ID", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (!(Pattern.matches("^[0-9]+$", price_txt.getText()))) {
                JOptionPane.showMessageDialog(this, "Please enter a price", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (evt.getSource() == add_btn) {
                model.insertRow(model.getRowCount(), new Object[]{id_txt.getText(), itemname_txt.getText(), price_txt.getText(), category_cbox.getSelectedItem(), java.time.LocalDate.now()});
                blank();
            }
            try {
                File data = new File("Files/foodItem.txt");
                if (!data.exists()) {
                    data.createNewFile();
                }

                try (FileWriter fw = new FileWriter(data.getAbsoluteFile())) {
                    BufferedWriter bw = new BufferedWriter(fw);

                    for (int i = 0; i < fooditem_tbl.getRowCount(); i++) {
                        for (int j = 0; j < fooditem_tbl.getColumnCount(); j++) {
                            bw.write(fooditem_tbl.getModel().getValueAt(i, j) + ",");
                        }
                        bw.write("\n");
                    }
                    bw.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(Food.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        try {
            File data = new File("Resources/onlyFoodItems.txt");
            if (!data.exists()) {
                data.createNewFile();
            }

            FileWriter fw = new FileWriter(data.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);

            int i = 0;
            for (i = 1; i <= fooditem_tbl.getRowCount(); i++) {
            }
            bw.write(--i + "");

            bw.close();
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(Food.class.getName()).log(Level.SEVERE, null, ex);
            ex.printStackTrace();
        }
        blank();
        id_txt.requestFocusInWindow();
    }//GEN-LAST:event_add_btnKeyPressed

